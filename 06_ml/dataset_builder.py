from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Dict, List

import numpy as np
import pandas as pd
from backend.core.storage import safe_read_dataframe, safe_write_dataframe

try:
    from backend.core.logging_config import get_logger
except Exception:  # pragma: no cover
    import logging
    logging.basicConfig(level=logging.INFO)
    def get_logger(name: str):
        return logging.getLogger(name)

logger = get_logger("matchflow.ml.dataset_builder")

REQUIRED_COLUMNS = ["date", "league", "season", "team_key", "opponent_key", "side"]
TARGET_COLUMNS = ["target_goals", "target_corners", "target_shots", "target_btts"]
RAW_OUTCOME_COLUMNS = {
    "goals_for_ft", "goals_against_ft", "total_goals_ft", "goals_for_ht", "goals_against_ht", "total_goals_ht",
    "shots_for", "shots_against", "shots_on_target_for", "shots_on_target_against", "corners_for", "corners_against",
    "result_ft", "result_ht", "points", "is_win", "is_draw", "is_loss",
}

class MLDatasetBuilder:
    """
    Builds a supervised ML research dataset using strict horizon targets.

    Anti-leakage rule implemented here:
    - Features are the values already present on row t. In this project those columns are historical/rolling features.
    - Targets are explicitly shifted to the next team match: X(t) -> y(t+1).
    - Raw current-match outcomes are kept only for audit/context and excluded later by FeatureSelector.
    - No operational signal is generated by this module.
    """

    def __init__(self, project_root: Path, config: Dict[str, Any]) -> None:
        self.project_root = project_root
        self.config = config
        self.input_path = self._resolve(config.get("input_dataset_path", "data/features/team_dataset_advanced.parquet"))
        self.output_path = self._resolve(config.get("output_dataset_path", "data/ml/datasets/ml_dataset.parquet"))
        self.horizon = int(config.get("target_horizon_matches", 1))

    def _resolve(self, value: str | Path) -> Path:
        path = Path(value)
        return path if path.is_absolute() else self.project_root / path

    def build(self) -> pd.DataFrame:
        logger.info("Iniciando build do dataset supervisionado de ML: %s", self.input_path)
        if not self.input_path.exists():
            raise FileNotFoundError(f"Advanced team dataset not found: {self.input_path}")

        df = safe_read_dataframe(self.input_path)
        if df.empty:
            raise ValueError("Advanced team dataset is empty.")

        missing = [col for col in REQUIRED_COLUMNS if col not in df.columns]
        if missing:
            raise ValueError(f"Missing required columns for ML dataset: {missing}")

        df = df.copy()
        df["date"] = pd.to_datetime(df["date"], errors="coerce")
        df = df[df["date"].notna()].sort_values(["team_key", "date", "league", "opponent_key"]).reset_index(drop=True)
        df = self._create_future_targets(df)
        df = self._add_audit_columns(df)

        self.output_path.parent.mkdir(parents=True, exist_ok=True)
        safe_write_dataframe(df, self.output_path, index=False)
        metadata = {
            "rows": int(len(df)),
            "columns": int(len(df.columns)),
            "target_columns": TARGET_COLUMNS,
            "target_policy": f"X(t) -> y(t+{self.horizon}) by team_key chronological order",
            "research_only": True,
            "output_path": str(self.output_path),
            "leakage_policy": "Targets are shifted forward by team. FeatureSelector excludes raw current-match outcomes and admits only historical/rolling/lagged features."
        }
        self.output_path.with_suffix(".metadata.json").write_text(json.dumps(metadata, indent=2, ensure_ascii=False), encoding="utf-8")
        logger.info("ML dataset criado: linhas=%s colunas=%s path=%s", len(df), len(df.columns), self.output_path)
        return df

    def _create_future_targets(self, df: pd.DataFrame) -> pd.DataFrame:
        outcome = self._current_outcome_targets(df)
        ordered = df.copy()
        for col in outcome.columns:
            ordered[f"current_{col}"] = outcome[col]
            ordered[col] = ordered.groupby("team_key", group_keys=False)[f"current_{col}"].shift(-self.horizon)

        ordered["target_match_date"] = ordered.groupby("team_key", group_keys=False)["date"].shift(-self.horizon)
        for context_col in ["league", "opponent_key", "side"]:
            ordered[f"target_{context_col}"] = ordered.groupby("team_key", group_keys=False)[context_col].shift(-self.horizon)

        logger.info(
            "Targets futuros ML criados com horizonte=%s: goals=%s corners=%s shots=%s btts=%s",
            self.horizon,
            int(ordered["target_goals"].notna().sum()),
            int(ordered["target_corners"].notna().sum()),
            int(ordered["target_shots"].notna().sum()),
            int(ordered["target_btts"].notna().sum()),
        )
        return ordered

    def _current_outcome_targets(self, df: pd.DataFrame) -> pd.DataFrame:
        def s(col: str) -> pd.Series:
            return pd.to_numeric(df[col], errors="coerce") if col in df.columns else pd.Series(np.nan, index=df.index)

        total_goals = s("total_goals_ft")
        if total_goals.isna().all():
            total_goals = s("goals_for_ft") + s("goals_against_ft")

        total_corners = s("corners_for") + s("corners_against")
        shots_for = s("shots_for")
        goals_for = s("goals_for_ft")
        goals_against = s("goals_against_ft")

        return pd.DataFrame({
            "target_goals": np.where(total_goals.notna(), (total_goals > 2.5).astype(int), np.nan),
            "target_corners": np.where(total_corners.notna(), (total_corners > 10.5).astype(int), np.nan),
            "target_shots": np.where(shots_for.notna(), (shots_for >= 10).astype(int), np.nan),
            "target_btts": np.where(goals_for.notna() & goals_against.notna(), ((goals_for > 0) & (goals_against > 0)).astype(int), np.nan),
        }, index=df.index)

    def _add_audit_columns(self, df: pd.DataFrame) -> pd.DataFrame:
        df["ml_target_horizon_matches"] = self.horizon
        df["ml_research_only"] = True
        df["ml_leakage_guard"] = "X_t_to_y_t_plus_1"
        return df
